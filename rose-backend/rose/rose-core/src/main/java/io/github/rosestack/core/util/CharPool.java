package io.github.rosestack.core.util;

/**
 * char 常量池
 */
public abstract class CharPool {
    // 字母常量
    public static final char UPPER_A = 'A'; // 大写字母 A
    public static final char LOWER_A = 'a'; // 小写字母 a
    public static final char UPPER_Z = 'Z'; // 大写字母 Z
    public static final char LOWER_Z = 'z'; // 小写字母 z

    // 标点符号常量
    public static final char DOT = '.'; // 点号
    public static final char AT = '@'; // 符号 @
    public static final char LEFT_BRACE = '{'; // 左大括号
    public static final char RIGHT_BRACE = '}'; // 右大括号
    public static final char LEFT_BRACKET = '('; // 左括号
    public static final char RIGHT_BRACKET = ')'; // 右括号
    public static final char DASH = '-'; // 破折号
    public static final char PERCENT = '%'; // 百分号
    public static final char PIPE = '|'; // 竖线
    public static final char PLUS = '+'; // 加号
    public static final char QUESTION_MARK = '?'; // 问号
    public static final char EXCLAMATION_MARK = '!'; // 感叹号
    public static final char EQUALS = '='; // 等号
    public static final char AMPERSAND = '&'; // 与号
    public static final char ASTERISK = '*'; // 星号
    public static final char BACK_SLASH = '\\'; // 反斜杠
    public static final char COLON = ':'; // 冒号
    public static final char COMMA = ','; // 逗号
    public static final char DOLLAR = '$'; // 美元符号
    public static final char SLASH = '/'; // 斜杠
    public static final char HASH = '#'; // 井号
    public static final char HAT = '^'; // 脱字符
    public static final char LEFT_CHEV = '<'; // 左尖括号
    public static final char RIGHT_CHEV = '>'; // 右尖括号
    public static final char SEMICOLON = ';'; // 分号
    public static final char SINGLE_QUOTE = '\''; // 单引号
    public static final char BACKTICK = '`'; // 反引号
    public static final char SPACE = ' '; // 空格
    public static final char TILDA = '~'; // 波浪号
    public static final char LEFT_SQ_BRACKET = '['; // 左方括号
    public static final char RIGHT_SQ_BRACKET = ']'; // 右方括号
    public static final char UNDERSCORE = '_'; // 下划线

    // 特殊字符常量
    public static final char NEWLINE = '\n'; // 换行符
    public static final char NUL = '\0'; // 空字符
    public static final char N = 'n'; // 字符 n
    public static final char Y = 'y'; // 字符 y
    public static final char QUOTE = '\"'; // 双引号
    public static final char RETURN = '\r'; // 回车符
    public static final char TAB = '\t'; // 制表符
    public static final char ONE = '1'; // 数字 1
    public static final char ZERO = '0'; // 数字 0
    // @formatter:on
}
